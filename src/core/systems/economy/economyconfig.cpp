#pragma once

#include "core/systems/economy/economyconfig.h"

#include <algorithm>
#include <cmath>

namespace cqsp::core::systems {
double EconomyConfig::ProductionConfig::GetFactoryMinUtilization(double factory_size) const {
    return std::max(1.0, factory_min_utilization * factory_size);
}

double EconomyConfig::ProductionConfig::GetFactoryUtilizationDiff(double profit) const {
    return 1 + max_factory_delta / (1 + std::exp(-(profit * profit_multiplier))) - max_factory_delta / 2;
}
}  // namespace cqsp::core::systems
